int R, G, B;

void InAr(int *ar, int n)
{
  for(int i=1; i<=n; i++) cin>> ar[i];
}

ll mem[202][202][202];

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    
    int t;
    // cin>> t;
	t= 1;
    while(t--)
    {
        ll an=0;
        cin>> R>> G>> B;
        int r[R+1], g[G+1], b[B+1];
        r[0]=0, g[0]=0, b[0]=0;
        InAr(r, R);
        InAr(g, G);
        InAr(b, B);

        sort(r, r+R+1, greater<int>());
        sort(g, g+G+1, greater<int>());
        sort(b, b+B+1, greater<int>());

		for(int i=R; i>=0; i--)
			for(int j=G; j>=0; j--)
				for(int k=B; k>=0; k--)
				{
					mem[i][j][k]= max(mem[i+1][j+1][k]+r[i]*g[j], max(mem[i+1][j][k+1]+r[i]*b[k], mem[i][j+1][k+1]+g[j]*b[k]));
					an= max(an, mem[i][j][k]);
				}

		cout<< an<< "\n";
        
    }
}