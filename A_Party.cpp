int n;
vector<int> gr[2001];
int level[2001]= {1};

int bfs(int source)
{
    int maxm= -1*INT32_MAX;
    queue<int> s;
    s.push(source);
    level[source]= 1;
    while(!s.empty())
    {
        int p= s.front();
        s.pop();
        // cout<<p<< " "<<gr[p].size()<<" ";
        for(int i=0; i<gr[p].size(); i++)
        {
            // cout<< gr[p][i]<<  " ";
            level[gr[p][i]]=level[p]+1;
            maxm= max(maxm, level[gr[p][i]]);
            s.push(gr[p][i]);
        }
		maxm= max(maxm, level[p]);
        // cout<< "\n";
    }
    return maxm;
}

