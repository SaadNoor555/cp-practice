int mem[101][100001];
int n, w;
int coin[101];

int infCoin(int idx, int rem)
{
    if(!rem) return 1;
    if(idx==n || rem<0) return 0;
    
    if(mem[idx][rem]!=-1) return mem[idx][rem];
    int p1=0, p2=0, p3=0;
    if(coin[idx]<=rem)
        p1= infCoin(idx, rem-coin[idx]);

    p3= infCoin(idx+1, rem);

    mem[idx][rem]= p1+p3;
    return mem[idx][rem];
}

int sinCoin(int idx, int rem)
{
    if(!rem) return 1;
    if(idx==n || rem<0) return 0;
    if(mem[idx][rem]!=-1) return mem[idx][rem];
    int p1=0, p2=0, p3=0;
    if(coin[idx]<=rem)
        p1= sinCoin(idx+1, rem-coin[idx]);
    p3= sinCoin(idx+1, rem);

    mem[idx][rem]= p1+p2+p3;
    return mem[idx][rem];
}

